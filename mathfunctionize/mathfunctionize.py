def addition(a, b):
    return a + b

def subtraction(a, b):
    return a - b

def multiplication(a, b):
    return a * b

def division(a, b):
    return a / b

def power(a, b):
    return a ** b

def modulo(a, b):
    return a % b

def flatDivision(a, b):
    return a // b

def factorial(x):
    if x == 0:
        return 1
    else:
        return x * factorial(x-1)

